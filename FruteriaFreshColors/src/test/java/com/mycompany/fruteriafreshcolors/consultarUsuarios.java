    void refrescar() {
        try
        {
            DataInputStream entrada = new DataInputStream(new FileInputStream("usuarios.dat"));
            try
            {
                Empleado empleados = new Empleado();
                Cliente clientes = new Cliente();

                if (jLabel2.getText().equals("Clientes"))
                {
                    while (true)
                    {
                        clientes.setNombre(entrada.readUTF());
                        clientes.setApellidos(entrada.readUTF());
                        clientes.setNickname(entrada.readUTF());
                        clientes.setPassword(entrada.readUTF());
                        clientes.setEstado(entrada.readUTF());
                        clientes.setCategoria(entrada.readUTF());
                        
                        if (clientes.getCategoria().equals("Cliente"))
                        {
                          dtm.addRow(new Object[]{clientes.getNombre(),clientes.getApellidos(),clientes.getNickname(),clientes.getCategoria()});
                        }
                    }
                } 
                if (jLabel2.getText().equals("Empleados"))
                {
                    while (true)
                    {
                        empleados.setNombre(entrada.readUTF());
                        empleados.setApellidos(entrada.readUTF());
                        empleados.setNickname(entrada.readUTF());
                        empleados.setPassword(entrada.readUTF());
                        empleados.setEstado(entrada.readUTF());
                        empleados.setPuesto(entrada.readUTF());
                        
                        if (clientes.getCategoria().equals("Empleado"))
                        {
                          dtm.addRow(new Object[]{empleados.getNombre(),empleados.getApellidos(),empleados.getNickname(),empleados.getPuesto()});
                        }
                    }
                } 
               
            } catch (FileNotFoundException fnfe)
            {
                JOptionPane.showMessageDialog(null, "¡Archivo no encontrado!", "Archivo no encontrado",
                        JOptionPane.ERROR_MESSAGE);
            }
        }catch (FileNotFoundException fnfe)
        {
            JOptionPane.showMessageDialog(null, "¡Archivo no encontrado!", "Archivo no encontrado",
                JOptionPane.ERROR_MESSAGE);
    }
    catch (IOException eioe)
        {
            JOptionPane.showMessageDialog(null, "¡Error en el dispositivo de almacenamiento!",
                "Error en el dispositivo", JOptionPane.ERROR_MESSAGE);
        }

}

